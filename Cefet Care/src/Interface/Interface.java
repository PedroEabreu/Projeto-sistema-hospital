    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        
        nomeTemporario = CampoUser.getText();
        senhaTemporario = CampoSenha.getText();
        CampoUser.setText("");
        CampoSenha.setText("");
        
       if(login.consultarCadastro(nomeTemporario, senhaTemporario)!=true)
       {
            JOptionPane.showMessageDialog(null,"Usuário ou senha incorreta");    
          
           
       }
       
       else if(login.consultarCadastro(nomeTemporario, senhaTemporario))
       {
            usuarioTemporario = login.retornarUsuario(nomeTemporario);
       
                     
            if(usuarioTemporario instanceof Medico){        
                CardLayout c1 = (CardLayout) PainelPrincipal.getLayout();
                c1.show(PainelPrincipal,"Tela medico");
                nomeMedico.setText(nomeTemporario);
                 Medico medico = (Medico) usuarioTemporario;

                especialidadeTelaMedico.setText(medico.getEspecialidade());
                String numAux;
               numAux = Integer.toString(medico.getAvaliacao());
                Avaliacao.setText("Avalição: "+numAux+"★");
             }       
            else if(usuarioTemporario instanceof Paciente){           
                CardLayout c1 = (CardLayout) PainelPrincipal.getLayout();
                c1.show(PainelPrincipal,"Tela paciente");
                nomePacienteTela.setText(nomeTemporario);
           }
       }
    }//GEN-LAST:event_entrarActionPerformed

